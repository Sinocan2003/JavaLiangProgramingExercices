public class Q08 {

	public static void main(String[] args) {
		final double PI = 3.14159;
		 double radius = 6.5;
		 
		 double perimeter = 2 * radius * PI;
		 System.out.println("perimeter is " + perimeter);

		 double area = PI * radius * radius;
		 System.out.println("area is " + area);
	}

}
